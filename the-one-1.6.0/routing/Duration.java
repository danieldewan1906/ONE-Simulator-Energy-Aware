package routing;

/**
 *
 * @author DANIEL
 */
public class Duration {
    /** The start value */
	public double start;
	
	/** The end value */
	public double end;
	
	/**
	 * Standard constructor that assigns s to start and e to end.
	 * 
	 * @param s Initial start value
	 * @param e Initial end value
	 */
	public Duration(double s, double e) {start = s; end = e;}
}
